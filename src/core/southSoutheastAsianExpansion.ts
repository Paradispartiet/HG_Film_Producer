const IDENTITY_GUARDED_IDS: ReadonlySet<string> = new Set(
  southSoutheastAsianExpansionDefinitions.map((definition) => definition.id),
);

function normalizeSouthSoutheastAsianTitle(value: string): string {
  return value
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, " ")
    .trim();
}

function directorsOverlap(
  scenario: HistoricalFilmScenario,
  definition: ModernCanonExpansionDefinition,
): boolean {
  const scenarioDirectors = scenario.film.directors.map(normalizeSouthSoutheastAsianTitle);
  return definition.directors
    .map(normalizeSouthSoutheastAsianTitle)
    .some((director) => scenarioDirectors.includes(director));
}

export function scenarioMatchesSouthSoutheastAsianDefinition(
  scenario: HistoricalFilmScenario,
  definition: ModernCanonExpansionDefinition,
): boolean {
  if (scenario.id === definition.id) return true;
  if (scenario.film.year !== definition.year) return false;

  const scenarioTitles = [scenario.film.title, scenario.film.original_title]
    .map(normalizeSouthSoutheastAsianTitle);
  const definitionTitles = [definition.title, definition.originalTitle, ...(definition.aliases ?? [])]
    .map(normalizeSouthSoutheastAsianTitle);
  if (!scenarioTitles.some((title) => definitionTitles.includes(title))) return false;

  if (!IDENTITY_GUARDED_IDS.has(definition.id)) return true;

  const normalizedEnglishTitle = normalizeSouthSoutheastAsianTitle(definition.title);
  const identityTitles = [definition.originalTitle, ...(definition.aliases ?? [])]
    .map(normalizeSouthSoutheastAsianTitle)
    .filter((title) => title && title !== normalizedEnglishTitle);
  const originalTitleMatches = identityTitles.includes(
    normalizeSouthSoutheastAsianTitle(scenario.film.original_title),
  );

  return originalTitleMatches || directorsOverlap(scenario, definition);
}

export function getSouthSoutheastAsianExpansionDefinition(
  scenario: HistoricalFilmScenario,
): ModernCanonExpansionDefinition | undefined {
  return southSoutheastAsianExpansionDefinitions.find((definition) => (
    scenarioMatchesSouthSoutheastAsianDefinition(scenario, definition)
  ));
}

export function mergeSouthSoutheastAsianExpansion(
  baseScenarios: readonly HistoricalFilmScenario[],
): readonly HistoricalFilmScenario[] {
  const merged = [...baseScenarios];
  let nextPosition = Math.max(0, ...baseScenarios.map((scenario) => scenario.source.position)) + 1;
  for (const definition of southSoutheastAsianExpansionDefinitions) {
    if (merged.some((scenario) => scenarioMatchesSouthSoutheastAsianDefinition(scenario, definition))) continue;
    merged.push(createSouthSoutheastAsianScenario(definition, nextPosition));
    nextPosition += 1;
  }
  return merged;
}

